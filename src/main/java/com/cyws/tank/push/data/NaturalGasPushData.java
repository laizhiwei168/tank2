package com.cyws.tank.push.data;

public class NaturalGasPushData {

	private String  equipmentId;//设备号 
	private String  equipmentNo; // 设备编号
	private String  dataTime;// 时间
	private String  longitude;// 经度
	private String  latitude;// 纬度
	private String  liquidLevel;//液位
	private String  pressure;// 压力
	private String  mediumQuality;//质量
	private String  temperature;// 温度
	private String  battery;// 电池电量
	private String  qualityPercentage;// 质量百分比	
	
	public String getEquipmentId() {
		return equipmentId;
	}
	public void setEquipmentId(String equipmentId) {
		this.equipmentId = equipmentId;
	}
	public String getEquipmentNo() {
		return equipmentNo;
	}
	public void setEquipmentNo(String equipmentNo) {
		this.equipmentNo = equipmentNo;
	}
	public String getDataTime() {
		return dataTime;
	}
	public void setDataTime(String dataTime) {
		this.dataTime = dataTime;
	}
	public String getLongitude() {
		return longitude;
	}
	public void setLongitude(String longitude) {
		this.longitude = longitude;
	}
	public String getLatitude() {
		return latitude;
	}
	public void setLatitude(String latitude) {
		this.latitude = latitude;
	}
	public String getLiquidLevel() {
		return liquidLevel;
	}
	public void setLiquidLevel(String liquidLevel) {
		this.liquidLevel = liquidLevel;
	}
	public String getPressure() {
		return pressure;
	}
	public void setPressure(String pressure) {
		this.pressure = pressure;
	}
	public String getMediumQuality() {
		return mediumQuality;
	}
	public void setMediumQuality(String mediumQuality) {
		this.mediumQuality = mediumQuality;
	}
	public String getTemperature() {
		return temperature;
	}
	public void setTemperature(String temperature) {
		this.temperature = temperature;
	}
	public String getBattery() {
		return battery;
	}
	public void setBattery(String battery) {
		this.battery = battery;
	}
	public String getQualityPercentage() {
		return qualityPercentage;
	}
	public void setQualityPercentage(String qualityPercentage) {
		this.qualityPercentage = qualityPercentage;
	}	
	
}
